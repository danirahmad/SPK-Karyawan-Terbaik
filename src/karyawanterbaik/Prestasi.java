    public void tampil(){
        DefaultTableModel data = new DefaultTableModel();
        data.addColumn("NIP");
        data.addColumn("Nama");
        data.addColumn("Absensi");
        data.addColumn("Sasaran kerja");
        data.addColumn("Target");
        data.addColumn("Realisasi");
        data.addColumn("Integritas");
        data.addColumn("Orientasi terhadap kualitas");
        data.addColumn("Kolaborasi");
        data.addColumn("Orientasi kepada customer");
        data.addColumn("Komunikasi");
        data.addColumn("Perencanaan dan pengorganisasian");
        data.addColumn("Hasil");
        try{
            konek();
            String url = "SELECT * FROM prestasi ORDER BY `prestasi`.`nama` ASC;";
            Statement st = kon.createStatement();
            ResultSet rs = st.executeQuery(url);
            while (rs.next()) {
                data.addRow(new Object[]{rs.getString(2),rs.getString(3),rs.getString(4),
                rs.getString(5),rs.getString(6),rs.getString(7),rs.getString(8),rs.getString(9),rs.getString(10),
                rs.getString(11),rs.getString(12),rs.getString(13),rs.getString(14)});
            }
           jTable1.setModel(data);
        }catch (Exception e){
            
        }
    }
    
