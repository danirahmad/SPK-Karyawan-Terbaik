    public void simpan(){
        if (jTextField1.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "MAAF!!\nNIP Tidak boleh kosong!");
        }else if (jComboBox1.getSelectedItem() == "-Pilih-"){
            JOptionPane.showMessageDialog(this, "MAAF!!\nKriteria Absensi harus dipilih!");
        }else if (jComboBox2.getSelectedItem() == "-Pilih-"){
            JOptionPane.showMessageDialog(this, "MAAF!!\nKriteria Sasaran kerja harus dipilih!");
        }else if (jComboBox3.getSelectedItem() == "-Pilih-"){
            JOptionPane.showMessageDialog(this, "MAAF!!\nKriteria Target harus dipilih!");
        }else if (jComboBox4.getSelectedItem() == "-Pilih-"){
            JOptionPane.showMessageDialog(this, "MAAF!!\nKriteria Relasi harus dipilih!");
        }else if (jComboBox5.getSelectedItem() == "-Pilih-"){
            JOptionPane.showMessageDialog(this, "MAAF!!\nKriteria Integritas harus dipilih!");
        }else if (jComboBox6.getSelectedItem() == "-Pilih-"){
            JOptionPane.showMessageDialog(this, "MAAF!!\nKriteria Orientasi terhadap kualitas harus dipilih!");
        }else if (jComboBox7.getSelectedItem() == "-Pilih-"){
            JOptionPane.showMessageDialog(this, "MAAF!!\nKriteria kolaborasi harus dipilih!");
        }else if (jComboBox8.getSelectedItem() == "-Pilih-"){
            JOptionPane.showMessageDialog(this, "MAAF!!\nKriteria Orientasi terhadap customer harus dipilih!");
        }else if (jComboBox9.getSelectedItem() == "-Pilih-"){
            JOptionPane.showMessageDialog(this, "MAAF!!\nKriteria Komunikasi harus dipilih!");
        }else if (jComboBox1.getSelectedItem() == "-Pilih-"){
            JOptionPane.showMessageDialog(this, "MAAF!!\nKriteria Perencanaan dan pengorganisasian harus dipilih!");
        }else{
        try{
            konek();
            Statement st = kon.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM prestasi WHERE nip=\'"+jTextField1.getText()+"\';");
            if(rs.next()){
                st.executeUpdate("UPDATE prestasi SET nip=\'"+jTextField1.getText()+"\', nama=\'"+txtnama.getText()+"\', absensi=\'"+jComboBox1.getSelectedItem()+"\', sasaran_kerja=\'"+jComboBox2.getSelectedItem()+"\'"
                        + ", target=\'"+jComboBox3.getSelectedItem()+"\', realisasi=\'"+jComboBox4.getSelectedItem()+"\', integritas=\'"+jComboBox5.getSelectedItem()+"\', orientasi_terhadap_kualitas=\'"+jComboBox6.getSelectedItem()+"\'"
                        + ", kolaborasi=\'"+jComboBox7.getSelectedItem()+"\', orientasi_kepada_customer=\'"+jComboBox8.getSelectedItem()+"\', komunikasi=\'"+jComboBox9.getSelectedItem()+"\'"
                        + ", perencanaan_dan_pengorganisasian=\'"+jComboBox10.getSelectedItem()+"\', hasil=\'"+txtHasil.getText()+"\' WHERE nip=\'"+jTextField1.getText()+"\';");
                JOptionPane.showMessageDialog(this, "Perubahan Data Prestasi Berhasil Disimpan");
                tampil();
            }else {
                String url = "INSERT INTO prestasi (nip,nama,absensi,sasaran_kerja,target,realisasi,integritas,orientasi_terhadap_kualitas,kolaborasi,orientasi_kepada_customer,komunikasi,perencanaan_dan_pengorganisasian,hasil) "
                        + "VALUES (\'"+jTextField1.getText()+"\', \'"+txtnama.getText()+"\', \'"+jComboBox1.getSelectedItem()+"\', \'"+jComboBox2.getSelectedItem()+"\', \'"+jComboBox3.getSelectedItem()+"\', \'"+jComboBox4.getSelectedItem()+"\',"
                        + "\'"+jComboBox5.getSelectedItem()+"\', \'"+jComboBox6.getSelectedItem()+"\', \'"+jComboBox7.getSelectedItem()+"\', \'"+jComboBox8.getSelectedItem()+"\', \'"+jComboBox9.getSelectedItem()+"\',"
                        + "\'"+jComboBox10.getSelectedItem()+"\', \'"+txtHasil.getText()+"\')";
                st.executeUpdate(url);
                JOptionPane.showMessageDialog(this, "Data Prestasi Baru Telah Disimpan");
                tampil();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Periksa lagi database anda" + e);
            }
        }
    }
    
    public void hapus(){
        int respons = JOptionPane.showConfirmDialog(this,
            "Apakah anda yakin ingin menghapus data prestasi?\n",
        "Penghapusan Data",
        JOptionPane.YES_NO_OPTION,
        JOptionPane.WARNING_MESSAGE);

        if (respons == 0){
            try{
                konek();
                Statement st = kon.createStatement();
                st.executeUpdate("DELETE FROM prestasi WHERE nip=\'"+jTextField1.getText()+"\' AND nama=\'"+txtnama.getText()+"\' AND absensi=\'"+jComboBox1.getSelectedItem()+"\' "
                        + "AND  sasaran_kerja=\'"+jComboBox2.getSelectedItem()+"\' AND  target=\'"+jComboBox3.getSelectedItem()+"\' AND  realisasi=\'"+jComboBox4.getSelectedItem()+"\'"
                        + "AND  integritas=\'"+jComboBox5.getSelectedItem()+"\' AND  orientasi_terhadap_kualitas=\'"+jComboBox6.getSelectedItem()+"\' AND  kolaborasi=\'"+jComboBox7.getSelectedItem()+"\'"
                        + "AND  orientasi_kepada_customer=\'"+jComboBox8.getSelectedItem()+"\' AND  komunikasi=\'"+jComboBox9.getSelectedItem()+"\'"
                        + "AND  perencanaan_dan_pengorganisasian=\'"+jComboBox10.getSelectedItem()+"\' AND hasil=\'"+txtHasil.getText()+"\'");
                JOptionPane.showMessageDialog(rootPane, "Data Prestasi telah dihapus\n"
                        + "NIP :"+jTextField1.getText()+"\n"
                        + "Hasil :"+txtHasil.getText()+"\n");
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "Data dalam database tidak ditemukan"+e);
            }
            reset();
            tampil();
        }else {
            reset();
        }  
    }
    
    public void cari(){
        try{
            konek();
            Statement st = kon.createStatement();
            ResultSet rs = st.executeQuery("SELECT prestasi.nip, prestasi.nama, prestasi.absensi, prestasi.sasaran_kerja, prestasi.target,"
                    + "prestasi.realisasi, prestasi.integritas, prestasi.orientasi_terhadap_kualitas, prestasi.kolaborasi,"
                    + "prestasi.orientasi_kepada_customer, prestasi.komunikasi, prestasi.perencanaan_dan_pengorganisasian,"
                    + "prestasi.hasil FROM prestasi WHERE nip=\'"+jTextField1.getText()+"\'");
            rs.next();
            jTextField1.setText(rs.getString("prestasi.nip"));
            txtnama.setText(rs.getString("prestasi.nama"));
            jComboBox1.setSelectedItem(rs.getString("prestasi.absensi"));
            jComboBox2.setSelectedItem(rs.getString("prestasi.sasaran_kerja"));
            jComboBox3.setSelectedItem(rs.getString("prestasi.target"));
            jComboBox4.setSelectedItem(rs.getString("prestasi.realisasi"));
            jComboBox5.setSelectedItem(rs.getString("prestasi.integritas"));
            jComboBox6.setSelectedItem(rs.getString("prestasi.orientasi_terhadap_kualitas"));
            jComboBox7.setSelectedItem(rs.getString("prestasi.kolaborasi"));
            jComboBox8.setSelectedItem(rs.getString("prestasi.orientasi_kepada_customer"));
            jComboBox9.setSelectedItem(rs.getString("prestasi.komunikasi"));
            jComboBox10.setSelectedItem(rs.getString("prestasi.perencanaan_dan_pengorganisasian"));
            txtHasil.setText(rs.getString("prestasi.hasil"));
            rs.close();
            st.close();
            kon.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "NIP Tidak ada"+e);
        } 
    }
    
